/**
 * Hotel Booking Application Entry Point
 * This class represents the starting point of the
 * Book My Stay - Hotel Booking Management System.
 * It demonstrates how a Java program starts execution
 * using the main() method and prints a welcome message
 * to the console.
 *
 * @author R. Shirley
 * @version 1.0
 */

public class BookMyStayApp {

    /**
     * The main method is the entry point of the Java application.
     * The JVM starts execution from this method.
     *
     * @param args command line arguments
     */
    public static void main(String[] args) {

        // Printing welcome message to the console
        System.out.println("======================================");
        System.out.println("      Welcome to Book My Stay App     ");
        System.out.println("      Hotel Booking System v1.0       ");
        System.out.println("======================================");
        System.out.println("Application started successfully.");
        System.out.println("Thank you for using Book My Stay!");
    }
}